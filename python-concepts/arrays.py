## ARRAYS
# Arrays are used to store multiple values in one single variable.
# Arrays are created using square brackets. For example:
my_array = [1, 2, 3]
print(my_array)

# The above code will print [1, 2, 3]

# You can also use the len() function to get the length of an array. For example:
my_array = [1, 2, 3]
print(len(my_array))
# The above code will print 3

# You can also use indexing to access elements in an array. For example:
my_array = [1, 2, 3]
print(my_array[0])
# The above code will print 1

# You can also use negative indexing to access elements in an array. For example:
my_array = [1, 2, 3]
print(my_array[-1])
# The above code will print 3

# You can also use slicing to access elements in an array. For example:
my_array = [1, 2, 3]
print(my_array[1:])
# The above code will print [2, 3]

# Arrays can also contain different data types. For example:
my_array = [1, "hello", 3.14]
print(my_array)
# The above code will print [1, 'hello', 3.14]

# You can also use the append() method to add elements to an array. For example:
my_array = [1, 2, 3]
my_array.append(4)
print(my_array)
# The above code will print [1, 2, 3, 4]

# You can also use the pop() method to remove elements from an array. For example:
my_array = [1, 2, 3]
my_array.pop()
print(my_array)
# The above code will print [1, 2]

